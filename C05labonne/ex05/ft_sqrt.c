int	ft_sqrt(int nb)
{
	int b;
	int temp;

	temp = nb;
	b = 0;
	while (nb >= 0)
	{
		nb -= b;
		b++;
		nb -= b;
	}
	b--;
	if (b * b == temp)
		return (b);
	else
		return (0);
}
